/**
 * Datos primitivos
 */

// String
let nombre = 'Juan';
console.log(nombre);

// Number
let edad = 30;
console.log(edad);

// Boolean
let esMayor = true;
console.log(esMayor);

// Undefined
let sinValor;
console.log(sinValor);

// Null
let nulo = null;
console.log(nulo);

// Symbol
let simbolo = Symbol('mi simbolo');
console.log(simbolo);

// BigInt
let bigInt = 100
console.log(bigInt);

// typeof
console.log(typeof nombre);
console.log(typeof edad);
console.log(typeof esMayor);
console.log(typeof sinValor);
console.log(typeof nulo);
console.log(typeof simbolo);
console.log(typeof bigInt);